    @Override
    public Categoria criar() {
        this.limpaTela();

        Categoria categoria = null;    
        
        txtCategoria.setText("");
        txtCategoria.setEditable(true);
        txtCategoria.setEnabled(true);
        RadioSim.setEnabled(true);
        RadioNao.setEnabled(true);
        

        

        //btnCancelar.setVisible(true);
        this.setVisible(true);
        if(retornoOk){
            this.getScreenObject();
        }
        return categoria;
    }
    
    public Categoria getScreenObject(){

        Categoria novo = new Categoria();
        novo.setNome(txtCategoria.getText());
        if(RadioSim.isSelected()){
            novo.setControlado("SIM");
        }
        else{
            novo.setControlado("NÃO");  
        }
        
        return novo;
    }
    
    
